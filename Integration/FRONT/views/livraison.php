<br><br>


 <h4>Délai et modes de livraison:</h4><br>
La livraison est effectuée selon les modes suivants : <br><br>

-  Livraison par Rapid-Post  <br>
-  Livraison par transporteur express en journée ouvrable (de 8h à 17h00 du lundi au vendredi). Prévoyez d'être chez vous aux heures ouvrées ou de vous faire livrer sur le lieu de travail.  <br>
-  Mise à disposition dans un point relais. <br><br>

La livraison sera effectuée dans le délai indiqué lors de la commande et ce sur toute la Tunisie. 

<br><br>

 <h4>Livraison par transporteur:</h4> <br>

Le délai d'acheminement par le transporteur privé est le suivant : <br>
 entre 2 et 5 jours pour les produits en stock et entre 5 et 10 jours pour les produits en pré-commande.<br><br>

Livraison par transporteur express à votre adresse (professionnelle ou personnelle) : <br>
 livraison vers une adresse de destination en Tunisie. Les frais de traitement et d’expédition sont de 8 TND TTC (6 TND pour les commandes payées par carte bancaire). Ce forfait inclut la préparation et l’envoi de la commande.

